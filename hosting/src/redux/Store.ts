import * as Redux from "redux";

import { ReduxTemplateState, reduxTemplateReducer, } from "@/redux/ReduxTemplate";

import middlewareTemplate from "@/redux/MiddlewareTemplate";

// ----------------------------------------------------------------
// ----------------------------------------------------------------
// ----------------------------------------------------------------

// ストア状態受信構造体
export interface ReduxStoreState {
	stateTemplate: ReduxTemplateState;
}

// ----------------------------------------------------------------
// ----------------------------------------------------------------
// ----------------------------------------------------------------

// ストア作成
export const store: Redux.Store = Redux.createStore(Redux.combineReducers({
	stateTemplate: reduxTemplateReducer,
}), Redux.applyMiddleware(
	middlewareTemplate
));

